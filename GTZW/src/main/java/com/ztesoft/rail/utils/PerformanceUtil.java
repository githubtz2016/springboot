package com.ztesoft.rail.utils;

public class PerformanceUtil {
    private Integer line;
    private String communityName;
    private String indexData;
    private String ruLong;
    private String ruLat;

    public String getRuLong() {
        return ruLong;
    }

    public void setRuLong(String ruLong) {
        this.ruLong = ruLong;
    }

    public String getRuLat() {
        return ruLat;
    }

    public void setRuLat(String ruLat) {
        this.ruLat = ruLat;
    }

    public PerformanceUtil() {
    }

    public PerformanceUtil(Integer line, String communityName, String indexData) {
        this.line = line;


        this.communityName = communityName;
        this.indexData = indexData;
    }

    public Integer getLine() {
        return line;
    }

    public void setLine(Integer line) {
        this.line = line;
    }


    public String getCommunityName() {
        return communityName;
    }

    public void setCommunityName(String communityName) {
        this.communityName = communityName;
    }

    public String getIndexData() {
        return indexData;
    }

    public void setIndexData(String indexData) {
        this.indexData = indexData;
    }
}
